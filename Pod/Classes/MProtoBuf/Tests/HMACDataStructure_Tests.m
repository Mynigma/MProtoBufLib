#import "TestHelper.h"
#import <XCTest/XCTest.h>
#import "HMACDataStructure.h"

@interface HMACDataStructure_Tests : XCTestCase

@end

@implementation HMACDataStructure_Tests

- (void)testDataWrapping
{
    NSData* encryptedData = [TestHelper sampleData:@1];
    NSData* HMAC = [TestHelper sampleData:@2];
    NSString* version = [TestHelper sampleString:@1];
    
    HMACDataStructure* dataStructure = [[HMACDataStructure alloc] initWithEncryptedData:encryptedData HMAC:HMAC version:version];
    
    NSData* data = dataStructure.serialisedData;
    
    HMACDataStructure* parsedDataStructure = [HMACDataStructure deserialiseData:data];
    
    XCTAssertNotNil(parsedDataStructure);
    
    XCTAssertEqualObjects(dataStructure.encryptedData, parsedDataStructure.encryptedData);
    XCTAssertEqualObjects(dataStructure.HMAC, parsedDataStructure.HMAC);
    XCTAssertEqualObjects(dataStructure.version, parsedDataStructure.version);
}

- (void)testNilSafe
{
    HMACDataStructure* dataStructure = [[HMACDataStructure alloc] initWithEncryptedData:nil HMAC:nil version:nil];
    
    [dataStructure serialisedData];
}


@end
