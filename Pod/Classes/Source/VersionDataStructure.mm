#import "VersionDataStructure.h"
#import "encryption.pb.h"




@implementation VersionDataStructure


- (instancetype)initWithVersion:(NSString*)version
{
    self = [super init];
    if (self) {
        
        self.version = version;
    }
    return self;
}

+ (instancetype)deserialiseData:(NSData*)data
{
    VersionDataStructure* newStructure = [VersionDataStructure new];

    mynigma::versionData* versionData = new mynigma::versionData;
    
    versionData->ParseFromArray([data bytes], (int)[data length]);
    
    NSData* versionStringData = [[NSData alloc] initWithBytes:versionData->version().data() length:versionData->version().size()];
    NSString* versionString = [[NSString alloc] initWithData:versionStringData encoding:NSUTF8StringEncoding];
    [newStructure setVersion:versionString];
    
    if(versionData)
        delete versionData;
    
    return newStructure;
}

- (NSData *)serialisedData
{
    mynigma::versionData* protocVersionData = new mynigma::versionData;
   
    if(self.version)
        protocVersionData->set_version([self.version UTF8String]);
    
    int data_size = protocVersionData->ByteSize();
    void* version_data = malloc(data_size);
    protocVersionData->SerializeToArray(version_data, data_size);
    
    
    NSData* returnData = [[NSData alloc] initWithBytes:version_data length:data_size];;
    free(version_data);
    if(protocVersionData)
        delete protocVersionData;
    
    return returnData;
}

@end
